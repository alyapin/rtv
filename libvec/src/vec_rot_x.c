#include "libvec.h"

t_vec	vec_rot_x(t_vec v, double a)
{
	t_vec	r;

	r.x = v.x;
	r.y = v.y * cos(a) - v.z * sin(a);
	r.z = v.y * sin(a) + v.z * cos(a);
	return (r);
}
