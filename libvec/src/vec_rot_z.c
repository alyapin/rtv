#include "libvec.h"

t_vec	vec_rot_z(t_vec v, double a)
{
	t_vec	r;

	r.x = v.x * cos(a) - v.y * sin(a);
	r.y = v.x * sin(a) + v.y * cos(a);
	r.z = v.z;
	return (r);
}
