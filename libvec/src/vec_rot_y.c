#include "libvec.h"

t_vec	vec_rot_y(t_vec v, double a)
{
	t_vec	r;

	r.x = v.x * cos(a) + v.z * sin(a);
	r.y = v.y;
	r.z = v.z * cos(a) - v.x * sin(a);
	return (r);
}
